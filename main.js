function check() {
  // Цикл по всем массивам в baseArray
  for (let i = 0; i < baseArray.length; i++) {
    // Сверяем inputArray с i-м массивом в baseArray
    let isEqual = true;
    for (let j = 0; j < inputArray.length; j++) {
      if (inputArray[j] != baseArray[i][j]) {
        isEqual = false;
        break;
      }
    }

    // Если массивы равны, то выводим индекс i
    if (isEqual) {
      document.querySelector(
        "#baseOutput"
      ).textContent = `Ваша база номер: ${i}`;
      utube.classList.toggle("hidden");

      return;
    }
  }

  // Если ни один массив не подошел, то выводим сообщение
  document.querySelector("#baseOutput").textContent =
    "Неверная база, перепроверьте планировку";
}
